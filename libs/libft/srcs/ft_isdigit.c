int		ft_line_is_space(char *c)
{
	while (*c)
	{
		if (*c != ' ')
			return 0;
        c++;
	}
	return 1;
}

int		ft_isdigit(int c)
{
	return (c >= '0' && c <= '9' ? 1 : 0);
}
